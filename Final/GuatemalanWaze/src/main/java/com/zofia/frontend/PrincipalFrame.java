    /* 
     * Llena las cajas de seleccion con los 
     * lugares ingresados en el archivo de entrada
    */
    private void setPlaces(List<Vertex> vertex) {
        this.places = new String[vertex.size()];
        for (int i = 0; i < places.length; i++) {
            places[i] = vertex.get(i).getName();
        }
        origin.setModel(new DefaultComboBoxModel<>(places));
        destination.setModel(new DefaultComboBoxModel<>(places));
        movementRegistration.setModel(new DefaultComboBoxModel<>(places));
    }
    
    private void buttonAvailability() {
        this.fileButton.setEnabled(false);
        this.navigationButton.setEnabled(true);
    }
    
    private void fillVector() {
        this.vehicleRoutes = new String[] {"Mejor ruta segun gasolina", "Peor ruta segun gasolina",
            "Mejor ruta segun distancia", "Peor ruta segun distancia", "Mejor ruta segun gasolina y distancia",
            "Peor ruta segun gasolina y distancia"};
        this.walkingRoutes = new String[] {"Mejor ruta segun desgaste fisico", "Peor ruta segun desgaste fisico",
            "Mejor ruta segun distancia", "Peor ruta segun distancia", "Mejor ruta segun desgaste fisico y distancia",
            "Peor ruta segun desgaste fisico y distancia"};
    }
    
