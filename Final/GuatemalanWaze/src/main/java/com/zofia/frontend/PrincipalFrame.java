    private void fileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fileButtonActionPerformed
        // TODO add your handling code here:
        this.file = "";
        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Open settings file");
        if(chooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            this.path = chooser.getSelectedFile().getPath();
            try {
                this.file = fileDriver.readInputFile(path);
                this.lexer = new FileLexer(new StringReader(file));
                this.parser = new FileParser(lexer);
                this.parser.parse();
                
                this.driver = parser.getDriver();
                this.generalGraph = new Graph(driver.getVertexes(), driver.getEdges());
                setPlaces(driver.getVertexes());
                buttonAvailability();
                
            } catch (IOException ex) {
                System.out.println("No se encontro el archivo");
            } catch (Exception ex) {
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_fileButtonActionPerformed

